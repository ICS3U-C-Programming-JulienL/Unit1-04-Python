def main():
    print("4+3 = {}".format(4 + 3))
    print("4-3 = {}".format(4 - 3))
    print("4*3 = {}".format(4 * 3))
    print("4/3 = {}".format(4 / 3))
    print("4^3 = {}".format(4 ^ 3))
    print("4**3 = {}".format(4**3))
    print("4**0.5 = {}".format(4**0.5))


if __name__ == "__main__":
    main()
